#Faça um programa que leia um numero inteiro e mostre na tela o seu sucessor e seu antecessor.

n1 = int(input('Digite um numero: '))
suc = n1 + 1
ant = n1 - 1
print(f'O sucessor de {n1} é {suc}, eo antecessor é {ant}')
