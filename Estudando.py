#Crie um algoritimo que leia um numero e mostre o seu dobro triplo e raiz quadrada.

n1 = int(input('Digite um numero: '))
D = n1 * 2
T = n1 * 3
R = n1 ** (1/2)
print(f'O dobro de {n1} é {D}, o Triplo é {T} e a raiz quadrada é {R:.4}')

